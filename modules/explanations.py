        jumps = [self.calculate_jump_size(d) for d in discontinuities]
        valid_jumps = [j for j in jumps if j > 0 and not np.isnan(j)]
        
        if not valid_jumps:
            return None
        
        avg_jump = np.mean(valid_jumps)
        
